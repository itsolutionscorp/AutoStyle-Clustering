def hey(S):
    # if no letters then be that way
    if not S or all([x in string.whitespace for x in S]):
        return 'Fine. Be that way!'
    # If there are only capital letters, chill out.
    elif (any([unicode(x) in UNICODE_UPPER for x in S]) and
          all([unicode(x) not in UNICODE_LOWER for x in S])):
        return 'Whoa, chill out!'
    elif S[-1] == '?':
        return 'Sure.'
    else:
        return 'Whatever.'
