def hey(what):
	if what.isupper():			  #If he is being shouted at...
		return "Whoa, chill out!"
	elif len(what) != 0 and what[-1] == '?':  #if he is asked a question...
		return 'Sure.'
	elif what.isspace() or len(what) == 0:	  #if he isn't told anything...
		return 'Fine. Be that way!'
	else:
		return "Whatever."		#And if all else fails, then...
